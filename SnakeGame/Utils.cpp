bool IsCollide(int x10, int y10, int r1,
	int x20, int y20, int r2)
{
	double d = sqrt(pow(abs(x10 - x20), 2) + pow(abs(y10 - y20), 2));

	if (d > r1 + r2) 
		return false;
	return true;
}
